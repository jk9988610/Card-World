function extractHelpPanels() {
  const panels = [];
  const re = /\{\s*id:\s*"([^"]+)",\s*label:\s*"([^"]+)",\s*html:\s*`([\s\S]*?)`\s*,?\s*\}/g;
  let m;
  while ((m = re.exec(helpSrc))) {
    panels.push({ id: m[1], label: m[2], html: m[3].trim() });
  }
  return panels;
}

